/*
Problem Description
Print the first five letters of the English alphabet i.e. A, B, C, D and E.

Output Format
Print the characters in separate lines.

Example Output
A
B
C
D
E
 */

class PrintAlphabet {
    public static void main(String[] args) {
        for (char i = 'A'; i <= 'E'; i++) {
            System.out.println(i);
        }
    }
}
